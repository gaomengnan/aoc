        counter = 0

        while cur:
            counter += 1
            cur = cur.next

        print("count", counter)

        if counter == 1:
            return None

        mid = counter // 2

        slow = None
        fast = head

        idx = 0

        while True:
            if idx == mid:
                if slow is None:
                    break
                slow.next = fast.next
                break

            slow = fast

            fast = fast.next

            idx += 1

        util.print_linked_list(head)

        return head


